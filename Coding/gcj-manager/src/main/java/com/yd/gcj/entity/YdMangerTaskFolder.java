package com.yd.gcj.entity;

import java.util.Date;

/***
 * 任务（项目）文件夹
 * @author Administrator
 */
public class YdMangerTaskFolder {
	/**唯一标识符*/
	private Integer f_id;
	/**文件夹名称*/
	private String f_name;
	/**所属任务id*/
	private Integer f_tid;
	/**文件夹创建时间*/
	private Date f_create_time;
	/**
	 * 获取 唯一标识符
	 * @return f_id
	 */
	public Integer getF_id() {
		return f_id;
	}
	
	/**
	 * 设置 唯一标识符
	 * @param f_id
	 */
	public void setF_id(Integer f_id) {
		this.f_id = f_id;
	}
	
	/**
	 * 获取 文件夹名称
	 * @return f_name
	 */
	public String getF_name() {
		return f_name;
	}
	
	/**
	 * 设置 文件夹名称
	 * @param f_name
	 */
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	
	/**
	 * 获取 所属任务id
	 * @return f_tid
	 */
	public Integer getF_tid() {
		return f_tid;
	}
	
	/**
	 * 设置 所属任务id
	 * @param f_tid
	 */
	public void setF_tid(Integer f_tid) {
		this.f_tid = f_tid;
	}
	
	/**
	 * 获取 文件夹创建时间
	 * @return f_create_time
	 */
	public Date getF_create_time() {
		return f_create_time;
	}
	
	/**
	 * 设置 文件夹创建时间
	 * @param f_create_time
	 */
	public void setF_create_time(Date f_create_time) {
		this.f_create_time = f_create_time;
	}
	
}
