package com.yd.gcj.entity;

public class YdMangerAwards {

	/**编号（主键）*/
	private Integer awards_id;
	/**案例id*/
	private Integer awards_caseid;
	/**奖项名称*/
	private String awards_name;
	/**奖项描述*/
	private String awards_desc;
	/**信息创建时间*/
	private long awards_create_time;
	
	/**
	 * 获取 编号（主键）
	 * @return awards_id
	 */
	public Integer getAwards_id() {
		return awards_id;
	}
	
	/**
	 * 设置 编号（主键）
	 * @param awards_id
	 */
	public void setAwards_id(Integer awards_id) {
		this.awards_id = awards_id;
	}
	
	/**
	 * 获取 案例id
	 * @return awards_caseid
	 */
	public Integer getAwards_caseid() {
		return awards_caseid;
	}
	
	/**
	 * 设置 案例id
	 * @param awards_caseid
	 */
	public void setAwards_caseid(Integer awards_caseid) {
		this.awards_caseid = awards_caseid;
	}
	
	/**
	 * 获取 奖项名称
	 * @return awards_name
	 */
	public String getAwards_name() {
		return awards_name;
	}
	
	/**
	 * 设置 奖项名称
	 * @param awards_name
	 */
	public void setAwards_name(String awards_name) {
		this.awards_name = awards_name;
	}
	
	/**
	 * 获取 奖项描述
	 * @return awards_desc
	 */
	public String getAwards_desc() {
		return awards_desc;
	}
	
	/**
	 * 设置 奖项描述
	 * @param awards_desc
	 */
	public void setAwards_desc(String awards_desc) {
		this.awards_desc = awards_desc;
	}
	
	/**
	 * 获取 信息创建时间
	 * @return awards_create_time
	 */
	public long getAwards_create_time() {
		return awards_create_time;
	}
	
	/**
	 * 设置 信息创建时间
	 * @param awards_create_time
	 */
	public void setAwards_create_time(long awards_create_time) {
		this.awards_create_time = awards_create_time;
	}
	
}
