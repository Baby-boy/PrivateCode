package com.yd.gcj.entity;

public class YdMangerMsgSize{
	
	/**系统消息未读数量*/
	private int systemMsgSize = 0;
	/**任务未读消息数量*/
	private int taskMsgSize = 0;
	/**留言消息数量*/
	private int leaveMsgSize = 0;
	/**总未读消息数量*/
	private int countMsgSize = 0;
	
	/**
	 * 获取 系统消息未读数量
	 * @return systemMsgSize
	 */
	public int getSystemMsgSize() {
		return systemMsgSize;
	}
	
	/**
	 * 设置 系统消息未读数量
	 * @param systemMsgSize
	 */
	public void setSystemMsgSize(int systemMsgSize) {
		this.systemMsgSize = systemMsgSize;
	}
	
	/**
	 * 获取 任务未读消息数量
	 * @return taskMsgSize
	 */
	public int getTaskMsgSize() {
		return taskMsgSize;
	}
	
	/**
	 * 设置 任务未读消息数量
	 * @param taskMsgSize
	 */
	public void setTaskMsgSize(int taskMsgSize) {
		this.taskMsgSize = taskMsgSize;
	}
	
	/**
	 * 获取 留言消息数量
	 * @return leaveMsgSize
	 */
	public int getLeaveMsgSize() {
		return leaveMsgSize;
	}
	
	/**
	 * 设置 留言消息数量
	 * @param leaveMsgSize
	 */
	public void setLeaveMsgSize(int leaveMsgSize) {
		this.leaveMsgSize = leaveMsgSize;
	}
	
	/**
	 * 获取 总未读消息数量
	 * @return countMsgSize
	 */
	public int getCountMsgSize() {
		countMsgSize = systemMsgSize+taskMsgSize+leaveMsgSize;
		return countMsgSize;
	}
	
	/**
	 * 设置 总未读消息数量
	 * @param countMsgSize
	 */
	public void setCountMsgSize(int countMsgSize) {
		this.countMsgSize = countMsgSize;
	}
	
}
