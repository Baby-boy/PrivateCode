package com.glanway.ctrlhr.entity.vo;

/**
 * 说明 : 设备返回的数据
 */
public class DeviceToEmployeeVo {

	private Long id; // ID

	private String name;// 员工名称

	private String code;// 员工代号

	private Long deptId;

	private String deptName;// 部门名称

	private Long jobId;

	private String jobName;// 职位名称

	private Integer gatherMsgState;// 采集状态 1:未采集,2部分采集,3:已采集

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public Long getDeptId() {
		return deptId;
	}

	public void setDeptId(Long deptId) {
		this.deptId = deptId;
	}

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	public Long getJobId() {
		return jobId;
	}

	public void setJobId(Long jobId) {
		this.jobId = jobId;
	}

	public String getJobName() {
		return jobName;
	}

	public void setJobName(String jobName) {
		this.jobName = jobName;
	}

	public Integer getGatherMsgState() {
		return gatherMsgState;
	}

	public void setGatherMsgState(Integer gatherMsgState) {
		this.gatherMsgState = gatherMsgState;
	}

}
